export function getArticleBySlug(slug: string): Article | undefined {
  return articles.find(article => article.slug === slug)
}

export function getArticlesByCategory(category: string): Article[] {
  return articles.filter(article => article.category === category)
}

export function getRelatedArticles(slug: string, limit: number = 3): Article[] {
  const article = getArticleBySlug(slug)
  if (!article) return []
  
  return articles
    .filter(a => a.slug !== slug && a.category === article.category)
    .slice(0, limit)
}

export function getFeaturedArticles(limit: number = 2): Article[] {
  return articles.filter(a => a.featured).slice(0, limit)
}

export function searchArticles(query: string): Article[] {
  const q = query.toLowerCase()
  return articles.filter(a => 
    a.title.toLowerCase().includes(q) || 
    a.excerpt.toLowerCase().includes(q) ||
    a.content.toLowerCase().includes(q)
  )
}

export function getLatestArticles(limit: number = 3): Article[] {
  return [...articles].sort((a, b) => 
    new Date(b.publishDate).getTime() - new Date(a.publishDate).getTime()
  ).slice(0, limit)
}
